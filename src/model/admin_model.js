class Product {
    constructor(id, name, price, screen, frontCamera, backCamera, img, desc, type) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.screen = screen;
        this.frontCamera = frontCamera;
        this.backCamera = backCamera;
        this.img = img;
        this.desc = desc;
        this.type = type;
    }
}

export default Product;
